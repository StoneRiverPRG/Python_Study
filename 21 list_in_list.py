# 九九の2次元リストの別の書き方
kuku2 = []
for i in range(1, 10):
    k = []
    for j in range(1, 10):
        k.append(i * j)
    kuku2.append(k)
# print(kuku2)

# 同じことを下記の一行で書けるのがリスト内包表記
kuku3 = [[i * j for j in range(1, 10)] for i in range(1, 10)]
# print(kuku3)

# リスト内包表記まとめ
list3 = [i for i in range(1, 6)]
print(list3)
# [1, 2, 3, 4, 5]

list4 = [i * 2 for i in range(1, 6)]
print(list4)
# [2, 4, 6, 8, 10]
