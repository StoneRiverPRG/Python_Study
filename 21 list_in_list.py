# 2次元リスト　－　リストの要素にリスト

# リストおさらい
x = 100
a = [1, 2, 3, "四", x]
# リストの要素には数値も文字列も変数も入れられる
print(a)
print(a[3])
# [1, 2, 3, '四', 100]
# 四

list1 = [1, 2, 3]
# リストの要素にリストは？
list2 = [list1, 2, 3]
print(list2)
# [[1, 2, 3], 2, 3]
# 出来た！リストの中にリスト。
print(list2[0])
# [1, 2, 3]
# リストの要素を取り出したらリストが出てきた。
# 二重のリスト（二次元リスト）

# リストのリストの要素を取り出したい時は[]を2つ
list2[0][2] = 99
print(list2)
# [[1, 2, 99], 2, 3]


